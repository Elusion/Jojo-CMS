$table = 'redirect';
$query = "
    CREATE TABLE {redirect} (
      `redirectid` int(11) NOT NULL auto_increment,
      `rd_from` varchar(255) NOT NULL,
      `rd_to` varchar(255) NOT NULL,
      `rd_type` enum('301','302') NOT NULL,
      `rd_order` int(11) NOT NULL,
      `rd_notes` text NOT NULL,
      `rd_regex` smallint(4) NOT NULL default '0',
      PRIMARY KEY  (`redirectid`)
    ) ENGINE=InnoDB;";

/* Check table structure */
$result = Jojo::checkTable($table, $query);

/* Output result */
if (isset($result['created'])) {
    echo sprintf("Table <b>%s</b> Does not exist - created empty table.<br />", $table);
}

if (isset($result['added'])) {
    foreach ($result['added'] as $col => $v) {
        echo sprintf("Table <b>%s</b> column <b>%s</b> Does not exist - added.<br />", $table, $col);
    }
}

if (isset($result['different'])) Jojo::printTableDifference($table,$result['different']);